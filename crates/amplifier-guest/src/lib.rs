#[cfg(test)]
mod tool_tests {
    use super::*;
    use serde_json::json;
    use std::collections::HashMap;

    // A minimal test tool for verifying the trait contract.
    #[derive(Default)]
    struct EchoTool;

    impl Tool for EchoTool {
        fn name(&self) -> &str {
            "echo"
        }

        fn get_spec(&self) -> ToolSpec {
            let mut params = HashMap::new();
            params.insert("message".to_string(), json!({"type": "string"}));
            ToolSpec {
                name: "echo".to_string(),
                parameters: params,
                description: Some("Echoes the input".to_string()),
            }
        }

        fn execute(&self, input: Value) -> Result<ToolResult, String> {
            Ok(ToolResult {
                success: true,
                output: Some(input),
                error: None,
            })
        }
    }

    #[test]
    fn test_tool_trait_name() {
        let tool = EchoTool;
        assert_eq!(tool.name(), "echo");
    }

    #[test]
    fn test_tool_trait_get_spec() {
        let tool = EchoTool;
        let spec = tool.get_spec();
        assert_eq!(spec.name, "echo");
        assert!(spec.parameters.contains_key("message"));
        assert_eq!(spec.description, Some("Echoes the input".to_string()));
    }

    #[test]
    fn test_tool_trait_execute_success() {
        let tool = EchoTool;
        let input = json!({"message": "hello"});
        let result = tool.execute(input.clone());
        assert!(result.is_ok());
        let result = result.unwrap();
        assert!(result.success);
        assert_eq!(result.output, Some(input));
    }

    #[test]
    fn test_tool_trait_get_spec_empty_parameters() {
        let spec = ToolSpec {
            name: "noop".to_string(),
            parameters: HashMap::new(),
            description: None,
        };
        let json_str = serde_json::to_string(&spec).unwrap();
        let deserialized: ToolSpec = serde_json::from_str(&json_str).unwrap();
        assert!(deserialized.parameters.is_empty());
        // Verify the parameters field serializes as an empty object.
        let raw: serde_json::Value = serde_json::from_str(&json_str).unwrap();
        assert_eq!(raw["parameters"], json!({}));
    }

    #[test]
    fn test_tool_trait_execute_error() {
        #[derive(Default)]
        struct FailTool;

        impl Tool for FailTool {
            fn name(&self) -> &str {
                "fail"
            }
            fn get_spec(&self) -> ToolSpec {
                ToolSpec {
                    name: "fail".to_string(),
                    parameters: HashMap::new(),
                    description: None,
                }
            }
            fn execute(&self, _input: Value) -> Result<ToolResult, String> {
                Err("something went wrong".to_string())
            }
        }

        let tool = FailTool;
        let result = tool.execute(json!({}));
        assert!(result.is_err());
        assert_eq!(result.unwrap_err(), "something went wrong");
    }
}

