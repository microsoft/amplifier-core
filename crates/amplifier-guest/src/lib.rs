/// Trait for WASM guest tool implementations.
///
/// All methods are synchronous — WASM guests are single-threaded.
pub trait Tool {
    /// Returns the tool's name.
    fn name(&self) -> &str;

    /// Returns the tool specification (name, parameters, description).
    fn get_spec(&self) -> ToolSpec;

    /// Executes the tool with the given JSON input.
    fn execute(&self, input: Value) -> Result<ToolResult, String>;
}

/// Exports a [`Tool`] implementation as WASM guest entry points.
///
/// Creates a singleton instance via `OnceLock` and generates `#[no_mangle] pub extern "C"`
/// functions that the host can call to discover and invoke the tool.
///
/// # Usage
///
/// ```ignore
/// #[derive(Default)]
/// struct MyTool;
///
/// impl Tool for MyTool { /* ... */ }
///
/// export_tool!(MyTool);
/// ```
///
/// # Generated items
///
/// - `get_tool() -> &'static $tool_type` — returns the singleton instance
/// - `__amplifier_tool_get_spec_len() -> u32` — byte length of the JSON-serialized [`ToolSpec`]
/// - `__amplifier_tool_get_spec(ptr: *mut u8)` — writes serialized spec into caller-provided buffer
///
/// **Note:** This is a simplified scaffold. Real Component Model exports use `wit-bindgen`'s
/// `generate!` macro. The macro internals will be adjusted when compiling the first fixture,
/// but the module-author interface (`impl Tool` + `export_tool!`) will not change.
/// Exports a [`Tool`] implementation as WASM guest entry points.
///
/// On **native targets** (testing), generates `#[no_mangle] pub extern "C"` functions
/// for spec introspection (`__amplifier_tool_get_spec_len`, `__amplifier_tool_get_spec`).
///
/// On **wasm32 targets** (Component Model), generates `wit-bindgen` Guest trait
/// implementation and component exports. Requires the calling crate to declare
/// `mod bindings;` (generated by `cargo component`) and depend on `wit-bindgen-rt`.
///
/// # Usage
///
/// ```ignore
/// #[derive(Default)]
/// struct MyTool;
///
/// impl Tool for MyTool { /* ... */ }
///
/// export_tool!(MyTool);
/// ```
///
/// # Generated items
///
/// - `get_tool() -> &'static $tool_type` — returns the singleton instance
/// - (native) `__amplifier_tool_get_spec_len() -> u32` — byte length of the JSON-serialized [`ToolSpec`]
/// - (native) `__amplifier_tool_get_spec(ptr: *mut u8)` — writes serialized spec into caller-provided buffer
/// - (wasm32) `impl bindings::exports::amplifier::modules::tool::Guest` — Component Model bridge
/// - (wasm32) `bindings::export!` invocation — wires up component exports
#[macro_export]
macro_rules! export_tool {
    ($tool_type:ident) => {
        // Tool singleton, lazily initialised via Default.
        static __AMPLIFIER_TOOL: $crate::__macro_support::OnceLock<$tool_type> =
            $crate::__macro_support::OnceLock::new();

        /// Returns a reference to the tool singleton.
        fn get_tool() -> &'static $tool_type {
            __AMPLIFIER_TOOL
                .get_or_init(|| <$tool_type as ::std::default::Default>::default())
        }

        // ----- Native target: C-ABI exports for testing -----

        // Lazily cached JSON representation of the ToolSpec.
        #[cfg(not(target_arch = "wasm32"))]
        static __AMPLIFIER_SPEC_CACHE: $crate::__macro_support::OnceLock<::std::vec::Vec<u8>> =
            $crate::__macro_support::OnceLock::new();

        #[cfg(not(target_arch = "wasm32"))]
        fn __amplifier_cached_spec() -> &'static [u8] {
            __AMPLIFIER_SPEC_CACHE.get_or_init(|| {
                let spec = <$tool_type as $crate::Tool>::get_spec(get_tool());
                $crate::__macro_support::serde_json::to_vec(&spec)
                    .expect("ToolSpec serialization must not fail")
            })
        }

        /// Returns the byte length of the JSON-serialized [`ToolSpec`].
        #[cfg(not(target_arch = "wasm32"))]
        #[no_mangle]
        pub extern "C" fn __amplifier_tool_get_spec_len() -> u32 {
            __amplifier_cached_spec().len().try_into()
                .expect("serialized ToolSpec must fit in u32 (WASM linear memory is ≤ 4 GiB)")
        }

        /// Copies the JSON-serialized [`ToolSpec`] into the buffer at `ptr`.
        ///
        /// # Safety
        ///
        /// The caller must provide a non-null buffer of at least
        /// `__amplifier_tool_get_spec_len()` bytes.
        #[cfg(not(target_arch = "wasm32"))]
        #[no_mangle]
        pub unsafe extern "C" fn __amplifier_tool_get_spec(ptr: *mut u8) {
            if ptr.is_null() {
                return;
            }
            let json = __amplifier_cached_spec();
            ::std::ptr::copy_nonoverlapping(json.as_ptr(), ptr, json.len());
        }

        // ----- WASM target: Component Model exports -----

        #[cfg(target_arch = "wasm32")]
        impl bindings::exports::amplifier::modules::tool::Guest for $tool_type {
            fn get_spec() -> ::std::vec::Vec<u8> {
                let spec = <$tool_type as $crate::Tool>::get_spec(get_tool());
                $crate::__macro_support::serde_json::to_vec(&spec)
                    .expect("ToolSpec serialization must not fail")
            }

            fn execute(input: ::std::vec::Vec<u8>) -> ::core::result::Result<::std::vec::Vec<u8>, ::std::string::String> {
                let input_val: $crate::Value =
                    $crate::__macro_support::serde_json::from_slice(&input)
                        .map_err(|e| e.to_string())?;
                let result = <$tool_type as $crate::Tool>::execute(get_tool(), input_val)?;
                $crate::__macro_support::serde_json::to_vec(&result)
                    .map_err(|e| e.to_string())
            }
        }

        #[cfg(target_arch = "wasm32")]
        bindings::export!($tool_type with_types_in bindings);
    };
}

